void Estimator::calMapPointErrorMean()
{
      for (auto &it_per_id : f_manager.feature)
      {
        it_per_id.used_num = it_per_id.feature_per_frame.size();
        if (!(it_per_id.used_num >= 2 && it_per_id.start_frame < WINDOW_SIZE - 2))//最后三帧中的新加特征点是不进行三角化的
            continue;

        if (it_per_id.estimated_depth > 0 && it_per_id.mp_estimation_error_mean.norm() > 1e-6)//--------------->add position
            continue;
        int imu_i = it_per_id.start_frame, imu_j = imu_i - 1;

        /***********calculate the mean value of unbias distribution***********START************Date:2019.06.17*********************** 
         *
         *Author: kang
         *pipeline: The mean value of samples is the unbiased estimation of the real distribution, we calculate several values of map points according to different view images.
         */

         //choose different images to triangulate the map point
         double sum_depth = 0.0;
         int cal_num = 0;
        
           for(int i = imu_i; i < imu_i + it_per_id.used_num; i++)
           {
             for(int j = i + 1; j < imu_i + it_per_id.used_num; j++)
             {
               //triangulate the map point
               ROS_ASSERT(NUM_OF_CAM == 1);

               Eigen::Matrix<double, 3, 4> P0;
               Eigen::Vector3d t0 = Ps[i] + Rs[i] * tic[0];
               Eigen::Matrix3d R0 = Rs[i] * ric[0];
               P0.leftCols<3>() = Eigen::Matrix3d::Identity();
               P0.rightCols<1>() = Eigen::Vector3d::Zero();

               Matrix4d design_matrix = Matrix4d::Zero();//used for triangulation between the start_frame and the last_observed_frame--------------->add position

               auto it_per_frame0 = it_per_id.feature_per_frame.begin() + i - imu_i;
               auto it_per_frame = it_per_id.feature_per_frame.begin() + j - imu_i;

               Eigen::Vector3d t1 = Ps[j] + Rs[j] * tic[0];
               Eigen::Matrix3d R1 = Rs[j] * ric[0];
               Eigen::Vector3d t = R0.transpose() * (t1 - t0);
               Eigen::Matrix3d R = R0.transpose() * R1;
               Eigen::Matrix<double, 3, 4> P;
               P.leftCols<3>() = R.transpose();
               P.rightCols<1>() = -R.transpose() * t;
               Eigen::Vector3d f0 = it_per_frame0->point.normalized();
               Eigen::Vector3d f = it_per_frame->point.normalized();
                
               design_matrix.row(0) = f0[0] * P0.row(2) - f0[2] * P0.row(0);
	           design_matrix.row(1) = f0[1] * P0.row(2) - f0[2] * P0.row(1);
	           design_matrix.row(2) = f[0] * P.row(2) - f[2] * P.row(0);
	           design_matrix.row(3) = f[1] * P.row(2) - f[2] * P.row(1);			     
           
	           Vector4d triangulated_point;
               Vector3d standard_point;
	           triangulated_point = design_matrix.jacobiSvd(Eigen::ComputeFullV).matrixV().rightCols<1>();
               sum_depth += triangulated_point(2)/triangulated_point(3);
               cal_num++;
            }
           }
                    
           sum_depth += it_per_id.estimated_depth;
           cal_num++;
           cout << "original depth is " << it_per_id.estimated_depth <<endl;
           cout << "bias depth is " << sum_depth/cal_num <<endl;
           //5: for MH01***3: for MH03****5:for MH04***3:for MH05**11:for V1_01_easy****1: for V1_02_medium***1: for V1_03_difficult****5:for V2_02_medium
           if(sum_depth/cal_num > 0 && abs(it_per_id.estimated_depth - sum_depth/cal_num) < 3) 
            {
           it_per_id.mp_estimation_error_mean(0) = 0;
           it_per_id.mp_estimation_error_mean(1) = 0;
           it_per_id.mp_estimation_error_mean(2) = it_per_id.estimated_depth - sum_depth/cal_num;
           it_per_id.estimated_depth = sum_depth/cal_num;
            }
         
         

        /***********calculate the mean value of unbias distribution***********END***********************************/   
      }
}

